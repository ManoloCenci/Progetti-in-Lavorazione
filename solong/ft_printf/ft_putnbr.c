#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	x;

	x = 0;
	if (n == -2147483648)
	{
		x += ft_putchar('-');
		x += ft_putchar('2');
		n = (147483648);
	}
	if (n < 0)
	{
		x += ft_putchar('-');
		n = -n;
	}
	if (n > 9)
		x += ft_putnbr(n / 10);
	x += ft_putchar(n % 10 + 48);
	return (x);
}
